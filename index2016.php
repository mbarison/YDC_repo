<P align="justify" STYLE="font-size: 12px;">For over 30 years, the Yellow Door Choir has been celebrating life and music through the presentation of choral repertoires in a variety of musical styles and languages.
<P align="justify" STYLE="font-size: 12px;">The choir is under the direction of <A HREF="http://yellowdoorchoir.com/choir.php?s=director" STYLE="color: #FFFFFF;">Roxanne Martel</a>. Roxanne is the music teacher and choral director at The Sacred Heart School of Montreal. She has sung with and conducted many choirs and ensembles in the Montreal area.
<P align="justify" STYLE="font-size: 12px;">We thank our former musical director, <A HREF="http://yellowdoorchoir.com/choir.php?s=director#eleanor" STYLE="color: #FFFFFF;">Dr. Eleanor Stubley</a>, who directed the Yellow Door Choir for the last sixteen years.  Under Eleanor's direction, the choir explored a rich variety of choral music from classic to contemporary while building community with over 30 Montreal charities. This achievement was recognized in 2012 when Eleanor received the Queen Elizabeth II Diamond Jubilee Medal.
<P align="justify" STYLE="font-size: 12px;">We continue to establish partnerships with local charities, using the high level of our artistic performances to raise funds for the charities and to profile their vital work. Over the years the choir has contributed to more than 60 community organizations, including Head and Hands, The Montreal Children’s Hospital, Mackay Rehabilitation Center, and Dans la rue.
      
